public class ExpressionDemo {
    public static void main(String[] args) {
        System.out.println("Integers");
        System.out.println(17 + 5);
        System.out.println(17 - 5);
        System.out.println(17 * 5);
        System.out.println(17 / 5);
        System.out.println(17 % 5);

        System.out.println("Doubles");
        System.out.println(17.0 + 5.0);
        System.out.println(17.0 - 5.0);
        System.out.println(17.0 * 5.0);
        System.out.println(17.0 / 5.0);
        System.out.println(17.0 % 5.0);
    }
}
